    public static int damage (int bound){
        Random rand = new Random();
        int damage = rand.nextInt(0, bound);
        
        if (damage == 0) {
            //say it missed
            return 0;
        }
        if (damage == bound - 1){
            //say critical hit
            return bound + 3;
        }
        return damage;
    }
    
